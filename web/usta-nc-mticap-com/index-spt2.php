          <h1>Program Description: CAP includes 3 distinct programs          </h1>
    	 <p><img src="images/divider.png" width="500" height="2" /></p>
    	 <p><strong>1. Certified Mental Coach (CMC):</strong> Online course that teaches coaches how to integrate mental training with their team. Written for MTI by one of the top sport psychology researchers in the world, Dr. Robert Weinberg, coaches learn how different athletes are motivated and how to work with them to achieve best results, reduce pre-game anxiety, enhance focus despite outside distractions, healthy discipline, and much more.    </p>
    	 <p><strong>2. Online Mental Trainer (OMT):</strong> Athletes get a custom program that guides their mental training throughout the year. OMT is fully internet-based, generates customized mental training programs, taught by experts using short videos, and enables coaches to track and support their athletes.</p>
   	        <p><strong>3. Parent Mental Trainer (PMT):</strong> Online course that teaches parents ideal support roles &amp; emotion control. PMT was written for MTI by the leading sport researchers in the world, Drs. Larry Lauer and Dan Gould at the Youth Sport Institute at Michigan State University.</p>
